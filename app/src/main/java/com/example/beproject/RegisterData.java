package com.example.beproject;

public class RegisterData {

    public String AadharNo, ContactNo, Email, Password;

    public RegisterData() {

    }

    public RegisterData(String aadharNo, String contactNo, String email, String password) {
        this.AadharNo = aadharNo;
        ContactNo = contactNo;
        Email = email;
        Password = password;
    }
}
